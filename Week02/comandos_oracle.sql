SELECT D.NOME NOME_DEPARTAMENTO,
       CHEFE.NOME CHEFE,
       ALOCACAO.NOME,
       PROJ.NOME PROJETO,
       PAP.NOME FUNCAO,
       TELC.NUMERO TELEFONE, 
       DEP.NOME DEPENDENTE
  FROM BRH.DEPARTAMENTO D
 INNER JOIN BRH.COLABORADOR CHEFE
    ON D.CHEFE = CHEFE.MATRICULA
 INNER JOIN BRH.COLABORADOR ALOCACAO 
    ON D.SIGLA = ALOCACAO.DEPARTAMENTO
 INNER JOIN BRH.TELEFONE_COLABORADOR TELC
    ON TELC.COLABORADOR = ALOCACAO.MATRICULA
  LEFT JOIN BRH.DEPENDENTE DEP
    ON DEP.COLABORADOR = ALOCACAO.MATRICULA
    LEFT JOIN BRH.ATRIBUICAO ATRI
    ON ATRI.COLABORADOR = ALOCACAO.MATRICULA
    INNER JOIN BRH.PAPEL PAP
    ON PAP.ID = ATRI.PAPEL
    INNER JOIN BRH.PROJETO PROJ
    ON PROJ.ID = ATRI.PROJETO

    
    
 ORDER BY D.NOME, ALOCACAO.NOME;